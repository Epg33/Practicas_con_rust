pub fn adding(num1:i32, num2:i32)->i32 {
  let result:i32 = num1+num2;
  return result
}

pub fn substrackting(num1:i32, num2:i32)->i32 {
  let result:i32 = num1-num2;
  return result
}

pub fn multipliying(num1:i32, num2:i32)->i32 {
  let result:i32 = num1*num2;
  return result
}

pub fn dividing(num1:i32, num2:i32)->i32 {
  let result:i32 = num1/num2;
  return result
}